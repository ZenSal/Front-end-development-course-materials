
function EvenOrOdd(num){
    if(num==0){
        console.log("This is zero");
        
    }else if(num<0){
        console.log("This is a negative number");
    }else if(num>0){
        console.log("This is a positive number");

    }else{
        console.log("This is a invalid input!!");
    }
}
EvenOrOdd(10);
EvenOrOdd(-2);
EvenOrOdd(0);
EvenOrOdd('adbc');
 

   